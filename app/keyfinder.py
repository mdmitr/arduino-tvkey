def process_sequence(key1, key2, key3, key4, seq_no, force = False):
  name = str(key1) + str(key2) + str(key3) + str(key4) + '.jpg'
  if force is False:
    if (os.path.isfile(name)):
      return False

  print(name)

  if force == True or seq_no % 2 == 0:
    send_command('p') # wake up
    time.sleep(7.5)

  send_command(str(key1))
  time.sleep(1.5)
  send_command(str(key2))
  time.sleep(1.5)
  send_command(str(key3))
  time.sleep(1.5)
  send_command(str(key4))
  time.sleep(3)
  print(", making shot, ", end='', flush=True)
  retval, frame = cam.read()
  if retval != True:
    raise ValueError("Can't read frame")
  imwrite(name, frame)

  if force == True or seq_no % 2 == 1:
    send_command('p')
    time.sleep(3)

  print('')

  return True

